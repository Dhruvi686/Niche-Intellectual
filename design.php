    <section class="tm-section" id="scope">
        <div class="container">
            <div class="row g-5">
                <!-- Left: What Can Be Registered -->
                <div class="col-lg-6" data-aos="fade-right">
                    <div class="tm-glass-card p-5 h-100" style="border-top: 4px solid var(--tm-accent-blue);">
                        <h4 class="text-white mb-4"><i class="fa-solid fa-circle-check text-info me-2"></i> What Can Be Registered?</h4>
                        <p class="text-white opacity-75 mb-4">A design is eligible for registration under the Designs Act if it fulfills these conditions:</p>
                        <ul class="list-unstyled text-white opacity-75 ps-2">
                            <li class="mb-3"><i class="fa-regular fa-square-check text-info me-2"></i> Is <strong>new and original</strong> (never published before).</li>
                            <li class="mb-3"><i class="fa-regular fa-square-check text-info me-2"></i> Has not been previously disclosed or published to the public.</li>
                            <li class="mb-3"><i class="fa-regular fa-square-check text-info me-2"></i> Relates specifically to the <strong>visual appearance</strong> of an article.</li>
                            <li class="mb-3"><i class="fa-regular fa-square-check text-info me-2"></i> Is capable of being applied by an industrial process.</li>
                            <li class="mb-3"><i class="fa-regular fa-square-check text-info me-2"></i> Appeals solely to the human eye.</li>
                        </ul>
                        <div class="mt-4 p-3 bg-dark bg-opacity-50 rounded">
                            <strong class="text-white d-block mb-2">Examples of registrable articles:</strong>
                            <span class="text-white opacity-50 small">Product packaging, bottles, containers, furniture, electronic gadgets, home decor items, fashion accessories, lighting fixtures, and industrial products.</span>
                        </div>
                    </div>
                </div>

                <!-- Right: What Cannot Be Registered -->
                <div class="col-lg-6" data-aos="fade-left">
                    <div class="tm-glass-card p-5 h-100" style="border-top: 4px solid var(--tm-accent-purple);">
                        <h4 class="text-white mb-4"><i class="fa-solid fa-circle-xmark text-danger me-2"></i> What Cannot Be Registered?</h4>
                        <p class="text-white opacity-75 mb-4">The following elements are generally excluded from design registration:</p>
                        <ul class="list-unstyled text-white opacity-75 ps-2">
                            <li class="mb-3"><i class="fa-regular fa-circle-xmark text-danger me-2"></i> <strong>Functional or technical features</strong> of a product.</li>
                            <li class="mb-3"><i class="fa-regular fa-circle-xmark text-danger me-2"></i> Mechanical devices or methods of construction.</li>
                            <li class="mb-3"><i class="fa-regular fa-circle-xmark text-danger me-2"></i> Trademarks, property marks, or artistic copyright works.</li>
                            <li class="mb-3"><i class="fa-regular fa-circle-xmark text-danger me-2"></i> Designs that are not substantially new or original.</li>
                            <li class="mb-3"><i class="fa-regular fa-circle-xmark text-danger me-2"></i> Designs contrary to public order, morality, or security.</li>
                        </ul>
                        <div class="mt-4 p-3 bg-dark bg-opacity-50 rounded">
                            <strong class="text-white d-block mb-2">Important Rule:</strong>
                            <span class="text-white opacity-50 small">Design protection covers aesthetic qualities ONLY. If your product features represent utility or mechanics, you should file a Patent instead of a Design.</span>
